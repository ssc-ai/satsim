def hosek_wilkie_luminance(
    target_zenith_deg,
    sun_zenith_deg,
    sun_sky_separation_deg,
    turbidity=_DEFAULT_TURBIDITY,
    ground_albedo=_DEFAULT_GROUND_ALBEDO,
):
    """Return Hosek-Wilkie CIE Y-channel sky luminance in cd/m^2."""
    if sun_zenith_deg >= 90.0 or target_zenith_deg >= 90.0:
        return 0.0

    solar_elevation = math.radians(90.0 - float(sun_zenith_deg))
    theta = math.radians(min(max(float(target_zenith_deg), 0.0), 89.9))
    gamma = math.radians(min(max(float(sun_sky_separation_deg), 0.0), 180.0))
    turbidity = min(max(float(turbidity), 1.0), 10.0)
    ground_albedo = min(max(float(ground_albedo), 0.0), 1.0)

    config = _cook_configuration(_DATASET_Y, turbidity, ground_albedo, solar_elevation)
    radiance = _cook_radiance_configuration(
        _DATASET_Y_RAD,
        turbidity,
        ground_albedo,
        solar_elevation,
    )
    y_radiance = _radiance_internal(config, theta, gamma) * radiance
    return max(0.0, y_radiance * _LUMINOUS_EFFICACY_LM_PER_W)


def default_turbidity():
    """Return the default Hosek-Wilkie atmospheric turbidity."""
    return _DEFAULT_TURBIDITY


def default_ground_albedo():
    """Return the default Hosek-Wilkie ground albedo."""
    return _DEFAULT_GROUND_ALBEDO


def _cook_configuration(dataset, turbidity, albedo, solar_elevation):
    int_turbidity = int(turbidity)
    turbidity_rem = turbidity - float(int_turbidity)
    solar_t = _solar_elevation_parameter(solar_elevation)
    config = [0.0] * 9

    _add_configuration(config, dataset, int_turbidity - 1, 0, (1.0 - albedo) * (1.0 - turbidity_rem), solar_t)
    _add_configuration(config, dataset, int_turbidity - 1, 1, albedo * (1.0 - turbidity_rem), solar_t)

    if int_turbidity < 10:
        _add_configuration(config, dataset, int_turbidity, 0, (1.0 - albedo) * turbidity_rem, solar_t)
        _add_configuration(config, dataset, int_turbidity, 1, albedo * turbidity_rem, solar_t)

    return config


def _cook_radiance_configuration(dataset, turbidity, albedo, solar_elevation):
    int_turbidity = int(turbidity)
    turbidity_rem = turbidity - float(int_turbidity)
    solar_t = _solar_elevation_parameter(solar_elevation)

    result = _radiance_configuration_sample(dataset, int_turbidity - 1, 0, solar_t) * (1.0 - albedo) * (1.0 - turbidity_rem)
    result += _radiance_configuration_sample(dataset, int_turbidity - 1, 1, solar_t) * albedo * (1.0 - turbidity_rem)

    if int_turbidity < 10:
        result += _radiance_configuration_sample(dataset, int_turbidity, 0, solar_t) * (1.0 - albedo) * turbidity_rem
        result += _radiance_configuration_sample(dataset, int_turbidity, 1, solar_t) * albedo * turbidity_rem

    return result


def _solar_elevation_parameter(solar_elevation):
    return (solar_elevation / (math.pi / 2.0)) ** (1.0 / 3.0)


def _add_configuration(config, dataset, turbidity_index, albedo_index, weight, solar_t):
    if weight == 0.0:
        return
    offset = 9 * 6 * 10 * albedo_index + 9 * 6 * turbidity_index
    basis = _bezier5_basis(solar_t)
    for i in range(9):
        config[i] += weight * sum(basis[j] * dataset[offset + i + 9 * j] for j in range(6))


def _radiance_configuration_sample(dataset, turbidity_index, albedo_index, solar_t):
    offset = 6 * 10 * albedo_index + 6 * turbidity_index
    basis = _bezier5_basis(solar_t)
    return sum(basis[i] * dataset[offset + i] for i in range(6))


def _bezier5_basis(t):
    u = 1.0 - t
    return (
        u ** 5,
        5.0 * u ** 4 * t,
        10.0 * u ** 3 * t ** 2,
        10.0 * u ** 2 * t ** 3,
        5.0 * u * t ** 4,
        t ** 5,
    )


def _radiance_internal(config, theta, gamma):
    cos_gamma = math.cos(gamma)
    exp_m = math.exp(config[4] * gamma)
    ray_m = cos_gamma * cos_gamma
    mie_base = max(1.0 + config[8] * config[8] - 2.0 * config[8] * cos_gamma, 1e-12)
    mie_m = (1.0 + cos_gamma * cos_gamma) / (mie_base ** 1.5)
    zenith = math.sqrt(max(math.cos(theta), 0.0))
    cos_theta = math.cos(theta)

    return (
        1.0 + config[0] * math.exp(config[1] / (cos_theta + 0.01))
    ) * (
        config[2]
        + config[3] * exp_m
        + config[5] * ray_m
        + config[6] * mie_m
        + config[7] * zenith
    )
